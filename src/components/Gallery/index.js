class Gallery extends Component {
  state = {startImage: imagesList[0].id}

  updateId = id => {
    this.setState({startImage: imagesList[id].id})
  }

  clickImage = () => {
    const {startImage} = this.state
    const resultList = imagesList.filter(each => each.id === startImage)
    return resultList
  }

  render() {
    const resultList = this.clickImage()

    const {startImage} = this.state

    return (
      <div className="con">
        <ul className="con3">
          {resultList.map(each => (
            <OneItem eachItemList={each} key={each.id} />
          ))}
          <h1 className="head">Nature Photography</h1>
          <p className="pa">Nature Photography by Rahul</p>
        </ul>

        <ul className="thumbnails">
          {imagesList.map(each => (
            <ThumbnailItem
              eachItem={each}
              key={each.id}
              updateId={this.updateId}
              isActive={startImage === each.id}
            />
          ))}
        </ul>
      </div>
    )
  }
}
export default Gallery
